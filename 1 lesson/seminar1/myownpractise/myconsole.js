
// Запросить пользователя ввести температуру в градусах Цельсия
let celsius = prompt("Введите температуру в градусах Цельсия:");

// Преобразовать введенное значение в число
celsius = parseFloat(celsius);

// Вычислить температуру в градусах Фаренгейта
let fahrenheit = (9 / 5) * celsius + 32;

// Вывести сообщение с результатом
alert("Цельсий: " + celsius + ", Фаренгейт: " + fahrenheit);